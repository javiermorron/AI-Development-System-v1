# ---------------------------------------------------------------------------
# Agent
# ---------------------------------------------------------------------------

@register
class BackendAgent(BaseAgent):
    """
    Generates a runnable FastAPI project from an architecture plan.
    Stage 2 of 3 in the example pipeline.
    """

    name = "backend-agent"
    allowed_tools = ["read_file", "write_file"]

    def execute(self, task: AgentTask) -> dict[str, Any]:
        arch = task.input.get("architecture")
        if not arch:
            raise ValueError("Input 'architecture' is required.")

        entities: list[str] = arch.get("entities", [])
        models: list[dict]  = arch.get("models", [])
        routes: list[dict]  = arch.get("routes", [])

        if not entities:
            raise ValueError("Architecture plan contains no entities.")

        project_name: str = arch.get("feature", "Generated API")

        files: dict[str, str] = {
            "main.py":            _main_py(entities, project_name),
            "models.py":          _models_py(models),
            "schemas.py":         _schemas_py(models),
            "services.py":        _services_py(entities),
            "repositories.py":    _repositories_py(entities),
            "routers/__init__.py": _routers_init_py(entities),
        }
        for entity in entities:
            files[f"routers/{entity.lower()}.py"] = _router_py(entity)

        files["requirements.txt"] = _requirements_txt(project_name)
        files["README.md"]        = _readme_md(project_name, entities)

        py_files = {k: v for k, v in files.items() if k.endswith(".py")}
        loc = sum(code.count("\n") for code in py_files.values())

        return {
            "files":                files,
            "entities_implemented": entities,
            "route_count":          len(routes),
            "lines_of_code":        loc,
        }
