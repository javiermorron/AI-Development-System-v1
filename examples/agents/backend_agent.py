def _models_py(models: list[dict]) -> str:
    """Pydantic BaseModel per entity."""
    lines = [
        '"""Domain models — generated by BackendAgent."""',
        "from __future__ import annotations",
        "",
        "from datetime import datetime",
        "from typing import Optional",
        "import uuid",
        "",
        "from pydantic import BaseModel, Field",
        "",
    ]
    for model in models:
        name: str = model["name"]
        fields: list[str] = model["fields"]
        lines += ["", f"class {name}(BaseModel):"]
        for field_decl in fields:
            fname, _, ftype = field_decl.partition(": ")
            default = _pydantic_default(fname, ftype)
            lines.append(f"    {field_decl} = {default}")
        lines += [
            "",
            "    class Config:",
            "        from_attributes = True",
        ]
    return "\n".join(lines) + "\n"


def _schemas_py(models: list[dict]) -> str:
    """Create / Update / Response schemas per entity."""
    lines = [
        '"""Request and response schemas — generated by BackendAgent."""',
        "from __future__ import annotations",
        "",
        "from datetime import datetime",
        "from typing import Optional",
        "",
        "from pydantic import BaseModel",
        "",
    ]
    for model in models:
        name: str = model["name"]
        all_fields: list[str] = model["fields"]

        # Writable fields: exclude auto-generated and secret fields
        writable = [f for f in all_fields if not _is_auto_field(f.split(":")[0])]
        # Response fields: exclude password-like fields
        response = [f for f in all_fields
                    if not f.split(":")[0].strip().endswith("_hash")]

        # --- Create schema ---
        lines += ["", f"class {name}Create(BaseModel):"]
        if writable:
            for field_decl in writable:
                lines.append(f"    {field_decl}")
        else:
            lines.append("    pass")

        # --- Update schema (all fields optional) ---
        lines += ["", f"class {name}Update(BaseModel):"]
        if writable:
            for field_decl in writable:
                fname, _, ftype = field_decl.partition(": ")
                if not ftype.startswith("Optional["):
                    lines.append(f"    {fname}: Optional[{ftype}] = None")
                else:
                    lines.append(f"    {field_decl} = None")
        else:
            lines.append("    pass")

        # --- Response schema ---
        lines += ["", f"class {name}Response(BaseModel):"]
        if response:
            for field_decl in response:
                lines.append(f"    {field_decl}")
        else:
            lines.append("    pass")
        lines += [
            "",
            "    class Config:",
            "        from_attributes = True",
        ]

    return "\n".join(lines) + "\n"


def _repositories_py(entities: list[str]) -> str:
    """In-memory CRUD repository per entity."""
    lines = [
        '"""Repositories — generated by BackendAgent."""',
        "from __future__ import annotations",
        "",
        "from typing import Any, Optional",
        "",
    ]
    for entity in entities:
        cls = f"{entity}Repository"
        ev = entity.lower()
        lines += [
            "",
            f"class {cls}:",
            f'    """In-memory store for {entity}. Replace with a DB adapter in v3."""',
            "",
            "    def __init__(self) -> None:",
            "        self._store: dict[str, Any] = {}",
            "",
            "    def find_by_id(self, id: str) -> Optional[Any]:",
            "        return self._store.get(id)",
            "",
            "    def find_all(self) -> list[Any]:",
            "        return list(self._store.values())",
            "",
            f"    def save(self, {ev}: Any) -> Any:",
            f"        self._store[{ev}.id] = {ev}",
            f"        return {ev}",
            "",
            "    def delete(self, id: str) -> bool:",
            "        if id in self._store:",
            "            del self._store[id]",
            "            return True",
            "        return False",
        ]
    return "\n".join(lines) + "\n"


def _services_py(entities: list[str]) -> str:
    """Service layer — one class per entity."""
    lines = [
        '"""Services — generated by BackendAgent."""',
        "from __future__ import annotations",
        "",
        "from typing import Optional",
        "",
    ]
    for entity in entities:
        svc = f"{entity}Service"
        repo_cls = f"{entity}Repository"
        ev = entity.lower()
        evpl = ev + "s"
        lines += [
            "",
            f"class {svc}:",
            f'    """Business logic for {entity}."""',
            "",
            f"    def __init__(self) -> None:",
            f"        from repositories import {repo_cls}",
            f"        self._repo = {repo_cls}()",
            "",
            f"    def list_{evpl}(self) -> list:",
            f'        """Return all {evpl}."""',
            f"        return self._repo.find_all()",
            "",
            f"    def get_{ev}(self, id: str) -> Optional[object]:",
            f'        """Return a single {ev} by id, or None."""',
            f"        return self._repo.find_by_id(id)",
            "",
            f"    def create_{ev}(self, data: dict) -> object:",
            f'        """Validate and persist a new {ev}."""',
            f"        # TODO: validate data, build {entity} model, save",
            f"        raise NotImplementedError",
            "",
            f"    def update_{ev}(self, id: str, data: dict) -> Optional[object]:",
            f'        """Apply a partial update to an existing {ev}."""',
            f"        # TODO: fetch existing, merge fields, validate, save",
            f"        raise NotImplementedError",
            "",
            f"    def delete_{ev}(self, id: str) -> bool:",
            f'        """Remove a {ev} permanently (Level-3 action)."""',
            f"        # TODO: check permissions before deletion",
            f"        return self._repo.delete(id)",
        ]
    return "\n".join(lines) + "\n"


def _router_py(entity: str) -> str:
    """FastAPI APIRouter for one entity."""
    ev = entity.lower()
    evpl = ev + "s"
    svc_cls = f"{entity}Service"
    lines = [
        f'"""Router for {entity} — generated by BackendAgent."""',
        "from __future__ import annotations",
        "",
        "from fastapi import APIRouter, HTTPException",
        "",
        f"from schemas import {entity}Create, {entity}Update, {entity}Response",
        f"from services import {svc_cls}",
        "",
        "router = APIRouter()",
        f"_service = {svc_cls}()",
        "",
        "",
        f'@router.get("/", response_model=list[{entity}Response])',
        f"def list_{evpl}() -> list:",
        f'    """List all {evpl}."""',
        f"    return _service.list_{evpl}()",
        "",
        "",
        f'@router.post("/", response_model={entity}Response, status_code=201)',
        f"def create_{ev}(data: {entity}Create) -> object:",
        f'    """Create a new {ev}."""',
        f"    try:",
        f"        return _service.create_{ev}(data.model_dump())",
        f"    except NotImplementedError:",
        f'        raise HTTPException(status_code=501, detail="Not yet implemented")',
        "",
        "",
        f'@router.get("/{{id}}", response_model={entity}Response)',
        f"def get_{ev}(id: str) -> object:",
        f'    """Get a single {ev} by id."""',
        f"    item = _service.get_{ev}(id)",
        f"    if item is None:",
        f'        raise HTTPException(status_code=404, detail="{entity} not found")',
        f"    return item",
        "",
        "",
        f'@router.put("/{{id}}", response_model={entity}Response)',
        f"def update_{ev}(id: str, data: {entity}Update) -> object:",
        f'    """Update an existing {ev}."""',
        f"    try:",
        f"        result = _service.update_{ev}(id, data.model_dump(exclude_unset=True))",
        f"    except NotImplementedError:",
        f'        raise HTTPException(status_code=501, detail="Not yet implemented")',
        f"    if result is None:",
        f'        raise HTTPException(status_code=404, detail="{entity} not found")',
        f"    return result",
        "",
        "",
        f'@router.delete("/{{id}}", status_code=204)',
        f"def delete_{ev}(id: str) -> None:",
        f'    """Delete a {ev}. Level-3 action — requires permission check."""',
        f"    if not _service.delete_{ev}(id):",
        f'        raise HTTPException(status_code=404, detail="{entity} not found")',
    ]
    return "\n".join(lines) + "\n"


def _routers_init_py(entities: list[str]) -> str:
    """routers/__init__.py — re-exports all entity routers."""
    lines = [
        '"""Router registry — generated by BackendAgent."""',
        "from __future__ import annotations",
        "",
    ]
    for entity in entities:
        ev = entity.lower()
        lines.append(f"from routers.{ev} import router as {ev}_router")
    lines += [
        "",
        "__all__ = [",
    ]
    for entity in entities:
        lines.append(f'    "{entity.lower()}_router",')
    lines.append("]")
    return "\n".join(lines) + "\n"


def _main_py(entities: list[str], project_name: str) -> str:
    """FastAPI application entry point."""
    lines = [
        f'"""FastAPI application — {project_name}"""',
        "from __future__ import annotations",
        "",
        "from fastapi import FastAPI",
        "",
    ]
    for entity in entities:
        ev = entity.lower()
        lines.append(f"from routers.{ev} import router as {ev}_router")
    lines += [
        "",
        f'app = FastAPI(title="{project_name}", version="0.1.0")',
        "",
        "",
        '@app.get("/health")',
        "def health() -> dict:",
        '    """Health check endpoint."""',
        '    return {"status": "ok"}',
        "",
    ]
    for entity in entities:
        ev = entity.lower()
        lines.append(
            f'app.include_router({ev}_router, prefix="/{ev}s", tags=["{ev}s"])'
        )
    return "\n".join(lines) + "\n"


