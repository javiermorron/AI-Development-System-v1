"""
BackendAgent — stage 2 of the example pipeline.

Input : {"architecture": <output from ArchitectureAgent>}
Output: {
    "files": {"models.py": str, "repositories.py": str,
              "services.py": str, "routes.py": str},
    "entities_implemented": list[str],
    "route_count": int,
    "lines_of_code": int,
}

Generates real, importable Python code from the architecture plan.
Replace the template engine with LLM calls in v3 for richer output.
"""

from __future__ import annotations

from typing import Any

from src.agents.base import AgentTask, BaseAgent
