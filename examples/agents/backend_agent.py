def _pydantic_default(field_name: str, field_type: str) -> str:
    """Return a Pydantic-compatible default expression for a field declaration."""
    fn = field_name.strip()
    ft = field_type.strip()

    if fn == "id":
        return 'Field(default_factory=lambda: str(uuid.uuid4()))'
    if ft == "datetime":
        return "Field(default_factory=datetime.utcnow)"
    if ft.startswith("Optional["):
        return "None"
    if ft.startswith("list["):
        return "Field(default_factory=list)"
    if ft == "dict":
        return "Field(default_factory=dict)"
    if ft == "bool":
        return "False"
    if ft == "int":
        return "0"
    if ft == "float":
        return "0.0"
    return '""'


def _is_auto_field(field_name: str) -> bool:
    """True for fields that should be excluded from Create/Update schemas."""
    fn = field_name.strip()
    return fn in ("id",) or fn.endswith("_at") or fn.endswith("_hash")


